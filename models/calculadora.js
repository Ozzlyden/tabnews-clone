
function somar(num1, num2) {
  return num1 + num2;
}

function subitracao(num1, num2) {
  return num1 - num2;
}

function divisao(num1, num2) {
  return num1 / num2;
}

function multiplicacao(num1, num2) {
  return num1 * num2;
}

exports.somar = somar;
exports.subitracao = subitracao;
exports.divisao = divisao;
exports.multiplicacao = multiplicacao;
